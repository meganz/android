package mega.privacy.android.app.presentation.permissions.model

/**
 * Requested permissions in app.
 */
enum class Permission {

    /**
     * Notifications permission.
     */
    Notifications,

    /**
     * Read permission.
     */
    Read,

    /**
     * Write permission.
     */
    Write,

    /**
     * Camera permission.
     */
    Camera,

    /**
     * Microphone permission.
     */
    Microphone,

    /**
     * Bluetooth permission.
     */
    Bluetooth,

    /**
     * Contacts permission.
     */
    Contacts
}